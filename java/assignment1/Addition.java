import java.lang.*;
class Addition{
    public static void main(String args[]){
        int a = 20;
        int b = 10;
        int c = a+b;
        System.out.println("The sum is " + c);
        int d = a-b;
        System.out.println("The subtraction is " + d);
        int e = a*b;
        System.out.println("The multiplication is " + e);
        int f = a/b;
        System.out.println("The division is " + f);
    }
}
import java.lang.*;
class Numcheck {
    public static void main(String args[]) {
        int a = -6;
        if (a < 0) {
            System.out.println("It's a negative number.");
        } else if (a > 0) {
            System.out.println("It's a positive number.");
        } else { 
            System.out.println("It's 0");
        }
    }
}
